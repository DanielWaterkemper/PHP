<?php
// Funçoes para strings

$nome = "daniel waterkemper";

echo strtoupper($nome); // função para deixar toda a string com letras maiusculas
echo "<br>" . strtolower($nome); // função para deixar toda a string com letras minusculas
echo "<br>" . ucfirst($nome); // função para deixar apenas a primeira letra maiuscula
echo "<br>" . ucwords($nome); // função para deixar apenas a primeira letra de cada palavra maiuscula


?>